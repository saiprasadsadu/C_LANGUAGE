//Write the calculator program using switch case statement
#include<stdio.h>
int main()
{
int a,b;
char ch;
switch(ch)
{
case '+':printf("%d",a+b);
break;
case '-':printf("%d",a-b);
break;
case '*':printf("%d",a*b);
break;
case '/':printf("%d",a/b);
break;
case '%':printf("%d",a%b);
break;
}
return 0;
}

